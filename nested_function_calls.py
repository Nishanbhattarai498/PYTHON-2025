print(round(abs(float(input("Enter a number: ")))))


#nested function calls are when a function is called inside another function
# in this case, the input function is called inside the float function
# and the float function is called inside the abs function
# and the abs function is called inside the round function
# this is a good example of nested function calls
# the input function takes a string as input and returns it as a string
# the float function takes a string as input and returns it as a float
# the abs function takes a float as input and returns its absolute value
# the round function takes a float as input and returns it as a rounded float
